#!/usr/bin/env python
"""Render docs/corpus_composition.md from data/manifest.json.

Kept separate from download_data.py so we can re-render the report (after a
narrative/formatting change) without re-walking the 646k docs.

The report has six sections:
    1. Headline numbers          (one row per language)
    2. Budget vs. availability   (does the corpus fit in 100M? by how much?)
    3. Category breakdown        (high-level source types per language)
    4. Data-source breakdown     (specific corpora used)
    5. Age-estimate metadata     (critical for the H4 acquisition-order analysis)
    6. Byte-premium sanity check (compares observed bytes/token to BP table)

Usage:
    python scripts/build_composition_report.py
    python scripts/build_composition_report.py --manifest data/manifest.json \\
        --output docs/corpus_composition.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO_ROOT))

from taam import BYTE_PREMIUM_HF, LANGUAGES  # noqa: E402

# Languages-of-interest order used throughout the paper.
LANG_DISPLAY = {"eng": "English (eng)", "nld": "Dutch (nld)", "zho": "Chinese (zho)"}
LANG_ORDER = ("eng", "nld", "zho")

TOKEN_BUDGET = 100_000_000  # BabyLM 2026 Multilingual track total budget


def _fmt_int(n: int) -> str:
    return f"{n:,}"


def _fmt_float(x: float, digits: int = 3) -> str:
    return f"{x:.{digits}f}"


def _top_table(counts: dict[str, int], total: int, n: int = 10) -> list[str]:
    """Render a markdown table of top-N counts with percentage of total."""
    lines = ["| Rank | Item | Documents | % of language |", "|---:|:---|---:|---:|"]
    for rank, (name, value) in enumerate(list(counts.items())[:n], start=1):
        pct = 100.0 * value / total if total else 0.0
        lines.append(f"| {rank} | `{name}` | {_fmt_int(int(value))} | {pct:.2f}% |")
    return lines


def _age_summary(by_age: dict[str, int], total_docs: int) -> tuple[int, int, list[str]]:
    """Return (num_with_age, num_without_age, top-bands-table-lines)."""
    no_age = int(by_age.get("n/a", 0)) + int(by_age.get("<unknown>", 0))
    with_age = total_docs - no_age
    bands_only = {k: v for k, v in by_age.items() if k not in {"n/a", "<unknown>"}}
    lines: list[str] = []
    if bands_only:
        lines = [
            "| Rank | Age band (months/years as reported) | Documents | % w/ age |",
            "|---:|:---|---:|---:|",
        ]
        for rank, (band, count) in enumerate(list(bands_only.items())[:10], start=1):
            pct = 100.0 * count / with_age if with_age else 0.0
            lines.append(f"| {rank} | `{band}` | {_fmt_int(int(count))} | {pct:.2f}% |")
    return with_age, no_age, lines


def render_report(manifest: dict) -> str:
    """Render the full Markdown report from a parsed manifest dict."""
    per_lang = manifest["per_language"]
    generated = manifest.get("generated_at", "n/a")

    lines: list[str] = []
    add = lines.append

    add("# BabyBabelLM Corpus Composition — EN / NL / ZH")
    add("")
    add(
        "Generated by `scripts/build_composition_report.py` from "
        "`data/manifest.json`."
    )
    add(f"Last manifest update: `{generated}`.")
    add("")
    add(
        "All figures below come from a single deterministic pass over the "
        "BabyBabelLM Tier-1 train splits (`BabyLM-community/babylm-{eng,nld,zho}`). "
        "Token counts use the `num-tokens` field shipped by the BabyBabelLM "
        "authors. Bytes are UTF-8 length of the `text` field."
    )
    add("")

    # -------- Section 1: Headline numbers --------
    add("## 1. Headline numbers")
    add("")
    add("| Language | Documents | Tokens | UTF-8 bytes | bytes/token | Avg chars/doc |")
    add("|:---|---:|---:|---:|---:|---:|")
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        p = per_lang[lang]
        bpt = p["bytes_per_token"] or 0.0
        add(
            f"| {LANG_DISPLAY[lang]} | "
            f"{_fmt_int(p['num_docs'])} | "
            f"{_fmt_int(p['num_tokens'])} | "
            f"{_fmt_int(p['num_bytes_utf8'])} | "
            f"{_fmt_float(bpt, 3)} | "
            f"{_fmt_float(p['chars_per_doc_avg'], 1)} |"
        )
    totals = manifest.get("totals", {})
    if totals:
        add(
            f"| **Total** | **{_fmt_int(totals['num_docs'])}** | "
            f"**{_fmt_int(totals['num_tokens'])}** | "
            f"**{_fmt_int(totals['num_bytes_utf8'])}** | — | — |"
        )
    add("")

    # -------- Section 2: Budget vs. availability --------
    add("## 2. Budget vs. availability (100M-token cap)")
    add("")
    add(
        f"The BabyLM 2026 Multilingual track sets a **{_fmt_int(TOKEN_BUDGET)} "
        "token budget across all three languages combined**. Byte-premium "
        "(BP) correction can shift this per-language. Available corpora:"
    )
    add("")
    add(
        "| Language | Available tokens | Budget if uniform (1/3) | BP-adjusted floor | Available / uniform |"
    )
    add("|:---|---:|---:|---:|---:|")
    uniform_share = TOKEN_BUDGET / 3
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        avail = per_lang[lang]["num_tokens"]
        bp = BYTE_PREMIUM_HF.get(lang, 1.0)
        # Byte-premium adjusted share: scale uniform_share by BP so that the
        # word-equivalent budget is constant across languages (per BabyLM rules).
        bp_share = uniform_share * bp
        ratio = avail / uniform_share if uniform_share else 0.0
        add(
            f"| {LANG_DISPLAY[lang]} | {_fmt_int(avail)} | "
            f"{_fmt_int(int(uniform_share))} | {_fmt_int(int(bp_share))} | "
            f"{ratio:.2f}x |"
        )
    add("")
    add(
        "> **Implication.** All three languages have at least "
        "~3x the uniform 1/3 share; the EXP3 mixer can freely re-weight in "
        "any direction without running out of data."
    )
    add("")

    # -------- Section 3: Category breakdown --------
    add("## 3. Category breakdown (high-level source types)")
    add("")
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        p = per_lang[lang]
        add(f"### {LANG_DISPLAY[lang]} — top categories")
        add("")
        for line in _top_table(p["by_category"], p["num_docs"], n=10):
            add(line)
        add("")

    # -------- Section 4: Data-source breakdown --------
    add("## 4. Data-source breakdown (specific corpora)")
    add("")
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        p = per_lang[lang]
        add(f"### {LANG_DISPLAY[lang]} — top data sources")
        add("")
        for line in _top_table(p["by_data_source"], p["num_docs"], n=10):
            add(line)
        add("")

    # -------- Section 5: Age-estimate metadata --------
    add("## 5. Age-estimate metadata (relevance for H4)")
    add("")
    add(
        "BabyBabelLM ships an `age-estimate` field per document, giving a "
        "coarse target age band (in years or year-ranges). This is "
        "complementary to our external child-acquisition norms in "
        "`analyses/acquisition_order/phenomenon_to_child_norm.yaml`."
    )
    add("")
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        p = per_lang[lang]
        n_age, n_no_age, table = _age_summary(p["by_age_estimate"], p["num_docs"])
        coverage = 100.0 * n_age / p["num_docs"] if p["num_docs"] else 0.0
        add(f"### {LANG_DISPLAY[lang]}")
        add("")
        add(
            f"- documents with explicit age band: "
            f"**{_fmt_int(n_age)}** ({coverage:.1f}%)"
        )
        add(f"- documents without age band (`n/a`): {_fmt_int(n_no_age)}")
        if table:
            add("")
            for line in table:
                add(line)
        else:
            add("- (no age bands available — all rows are `n/a`)")
        add("")

    add(
        "> **Implication for H4.** Dutch and Chinese carry rich, "
        "language-specific age annotations that we can use as a secondary "
        "validation signal alongside the external acquisition-order norms. "
        "For English (which is uniformly `n/a` here) we rely entirely on "
        "external norms (Brown 1973; Tomasello 2003; literature-derived "
        "phenomenon ages)."
    )
    add("")

    # -------- Section 6: BP sanity check --------
    add("## 6. Byte-premium sanity check")
    add("")
    add(
        "Compares our `bytes/token` observation against the BP table we use "
        "to set the EN/NL/ZH budget ratios. The ratios should track "
        "(but not equal) the canonical BP values; substantial deviations "
        "would indicate that the tokenizer assumed in BabyBabelLM's "
        "`num-tokens` differs from ours."
    )
    add("")
    eng_bpt = per_lang.get("eng", {}).get("bytes_per_token") or 1.0
    add(
        "| Language | observed bytes/token | normalized vs. EN | canonical BP (HF) |"
    )
    add("|:---|---:|---:|---:|")
    for lang in LANG_ORDER:
        if lang not in per_lang:
            continue
        bpt = per_lang[lang]["bytes_per_token"] or 0.0
        norm = bpt / eng_bpt if eng_bpt else 0.0
        bp = BYTE_PREMIUM_HF.get(lang, 1.0)
        add(f"| {LANG_DISPLAY[lang]} | {bpt:.3f} | {norm:.3f} | {bp:.3f} |")
    add("")

    return "\n".join(lines) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--manifest",
        type=Path,
        default=REPO_ROOT / "data" / "manifest.json",
        help="path to manifest JSON (default: data/manifest.json)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=REPO_ROOT / "docs" / "corpus_composition.md",
        help="path to write the Markdown report (default: docs/corpus_composition.md)",
    )
    args = parser.parse_args()

    if not args.manifest.exists():
        print(f"manifest not found: {args.manifest}", file=sys.stderr)
        print("Run scripts/download_data.py first.", file=sys.stderr)
        return 2

    manifest = json.loads(args.manifest.read_text())
    report = render_report(manifest)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report)
    print(f"Wrote {args.output.relative_to(REPO_ROOT)} ({len(report):,} chars).")
    return 0


if __name__ == "__main__":
    sys.exit(main())
