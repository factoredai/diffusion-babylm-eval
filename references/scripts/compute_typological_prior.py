"""Compute the URIEL/lang2vec typological prior for the TAAM TAAM project.

Usage:
    python scripts/compute_typological_prior.py \
        --languages eng nld zho \
        --feature-sets syntax_knn phonology_knn inventory_knn \
        --temperature 0.5 \
        --floor 0.10 \
        --output docs/typological_prior_report.md \
        --config-output configs/typological_prior.yaml

This is intentionally a *single source of truth*: the report (Markdown) is
human-readable for the paper appendix, and the YAML config is consumed by
the training pipeline (P1 and TAAM configs).
"""
from __future__ import annotations

import argparse
import json
import logging
import sys
from pathlib import Path

import numpy as np

# Make repo root importable when running this script directly.
REPO_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO_ROOT))

from taam.typological_prior import (  # noqa: E402
    DEFAULT_FEATURE_SETS,
    DEFAULT_FLOOR,
    DEFAULT_TEMPERATURE,
    compute_typological_prior,
)

LOG = logging.getLogger("typological_prior")


def render_markdown_report(result, byte_premia: dict[str, float]) -> str:
    """Render a self-contained Markdown report of the derived prior."""
    lines: list[str] = []
    lines.append("# Typological Prior — URIEL-derived (TAAM)")
    lines.append("")
    lines.append(
        "Auto-generated by ``scripts/compute_typological_prior.py``. "
        "**Do not edit by hand**; rerun the script to update."
    )
    lines.append("")
    lines.append("## Configuration")
    lines.append(f"- Languages (ISO 639-3): `{', '.join(result.languages)}`")
    lines.append(f"- Feature sets: `{', '.join(result.feature_sets)}`")
    lines.append(f"- Features used (after dropping unknowns): **{result.n_features_used}**")
    lines.append(f"- Softmax temperature: **{result.temperature}**")
    lines.append(f"- Probability floor: **{result.floor}**")
    lines.append("")

    lines.append("## Pairwise cosine distances")
    lines.append("")
    header = "| | " + " | ".join(f"`{l}`" for l in result.languages) + " |"
    sep = "|---|" + "|".join(["---:"] * len(result.languages)) + "|"
    lines.append(header)
    lines.append(sep)
    for i, li in enumerate(result.languages):
        cells = " | ".join(f"{result.pairwise_distance[i, j]:.4f}" for j in range(len(result.languages)))
        lines.append(f"| `{li}` | {cells} |")
    lines.append("")

    lines.append("## Per-language typological isolation")
    lines.append("")
    lines.append("Isolation s_l := mean cosine distance to the other languages in the set.")
    lines.append("")
    lines.append("| Language | Isolation s_l |")
    lines.append("|---|---:|")
    for l, s in zip(result.languages, result.isolation):
        lines.append(f"| `{l}` | {s:.4f} |")
    lines.append("")

    lines.append("## Derived initial sampling probabilities π_0")
    lines.append("")
    lines.append("Computed as ``pi_0 = softmax(s_l / T)`` with the configuration above, ")
    lines.append("then clipped to the floor and renormalized.")
    lines.append("")
    lines.append("| Language | π_0 (token-share) | Byte Premium (HF) | π_0 (byte-share) |")
    lines.append("|---|---:|---:|---:|")
    total_byte_weight = sum(p * byte_premia[l] for l, p in zip(result.languages, result.pi_0))
    for l, p in zip(result.languages, result.pi_0):
        bp = byte_premia[l]
        p_byte = (p * bp) / total_byte_weight
        lines.append(f"| `{l}` | {p:.4f} | {bp:.4f} | {p_byte:.4f} |")
    lines.append("")

    lines.append("## Sanity checks")
    lines.append("")
    zho_idx = result.languages.index("zho") if "zho" in result.languages else None
    eng_idx = result.languages.index("eng") if "eng" in result.languages else None
    nld_idx = result.languages.index("nld") if "nld" in result.languages else None
    if zho_idx is not None and eng_idx is not None and nld_idx is not None:
        p = result.pi_0
        ordering_ok = p[zho_idx] >= p[eng_idx] and p[zho_idx] >= p[nld_idx]
        status = "✅" if ordering_ok else "⚠️"
        lines.append(
            f"- {status} Expected ordering pi_0(zho) >= pi_0(eng), pi_0(nld): "
            f"**{'satisfied' if ordering_ok else 'NOT satisfied — escalate before training'}**."
        )
        lines.append(f"- π_0(zho) - π_0(eng) = {p[zho_idx] - p[eng_idx]:+.4f}")
        lines.append(f"- π_0(zho) - π_0(nld) = {p[zho_idx] - p[nld_idx]:+.4f}")
    lines.append(f"- Sum of π_0 = {result.pi_0.sum():.6f} (must be 1.0)")
    lines.append(f"- Minimum π_0 entry = {result.pi_0.min():.4f} (must be ≥ floor {result.floor})")
    lines.append("")

    lines.append("## Raw JSON")
    lines.append("")
    lines.append("```json")
    lines.append(json.dumps(result.as_dict(), indent=2))
    lines.append("```")
    lines.append("")
    return "\n".join(lines)


def _round_and_absorb_residual(values: list[float], decimals: int = 6) -> list[float]:
    """Round to ``decimals`` places, then absorb the residual into the largest entry.

    Guarantees the resulting list sums to exactly 1.0 (mod IEEE float).
    """
    import numpy as _np

    arr = _np.round(_np.asarray(values, dtype=_np.float64), decimals)
    residual = 1.0 - arr.sum()
    arr[int(arr.argmax())] += residual
    return [float(x) for x in arr]


def render_yaml_config(result, byte_premia: dict[str, float]) -> str:
    """Render the YAML config consumed by P1 and TAAM training configs."""
    total_byte_weight = sum(p * byte_premia[l] for l, p in zip(result.languages, result.pi_0))
    by_byte = {
        l: (p * byte_premia[l]) / total_byte_weight
        for l, p in zip(result.languages, result.pi_0)
    }

    pi_token_clean = dict(zip(
        result.languages,
        _round_and_absorb_residual([float(p) for p in result.pi_0]),
    ))
    pi_byte_clean = dict(zip(
        result.languages,
        _round_and_absorb_residual([float(by_byte[l]) for l in result.languages]),
    ))

    lines: list[str] = []
    lines.append("# Typological prior derived by scripts/compute_typological_prior.py")
    lines.append("# DO NOT EDIT BY HAND. Rerun the script to update.")
    lines.append("typological_prior:")
    lines.append(f"  feature_sets: {list(result.feature_sets)}")
    lines.append(f"  temperature: {result.temperature}")
    lines.append(f"  floor: {result.floor}")
    lines.append(f"  n_features_used: {result.n_features_used}")
    lines.append("  pi_0_tokens:")
    for l in result.languages:
        lines.append(f"    {l}: {pi_token_clean[l]:.6f}")
    lines.append("  pi_0_bytes:")
    for l in result.languages:
        lines.append(f"    {l}: {pi_byte_clean[l]:.6f}")
    lines.append("  isolation:")
    for l, s in zip(result.languages, result.isolation):
        lines.append(f"    {l}: {float(s):.6f}")
    lines.append("  byte_premium_used:")
    for l in result.languages:
        lines.append(f"    {l}: {float(byte_premia[l]):.6f}")
    lines.append("")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--languages",
        nargs="+",
        default=["eng", "nld", "zho"],
        help="ISO 639-3 codes; ordering preserved (default: eng nld zho)",
    )
    parser.add_argument(
        "--feature-sets",
        nargs="+",
        default=list(DEFAULT_FEATURE_SETS),
        help=f"URIEL feature families (default: {' '.join(DEFAULT_FEATURE_SETS)})",
    )
    parser.add_argument(
        "--temperature",
        type=float,
        default=DEFAULT_TEMPERATURE,
        help=f"Softmax temperature (default: {DEFAULT_TEMPERATURE})",
    )
    parser.add_argument(
        "--floor",
        type=float,
        default=DEFAULT_FLOOR,
        help=f"Probability floor per language (default: {DEFAULT_FLOOR})",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("docs/typological_prior_report.md"),
        help="Markdown report output path",
    )
    parser.add_argument(
        "--config-output",
        type=Path,
        default=Path("configs/typological_prior.yaml"),
        help="YAML config output path",
    )
    parser.add_argument("--verbose", "-v", action="store_true")
    args = parser.parse_args(argv)

    logging.basicConfig(
        level=logging.INFO if args.verbose else logging.WARNING,
        format="%(asctime)s %(levelname)s [%(name)s] %(message)s",
    )

    result = compute_typological_prior(
        languages=tuple(args.languages),
        feature_sets=tuple(args.feature_sets),
        temperature=args.temperature,
        floor=args.floor,
    )

    # Use BYTE_PREMIUM_HF as the canonical source (see improved_research_context_v2 §1.1).
    from taam import BYTE_PREMIUM_HF

    byte_premia = {l: BYTE_PREMIUM_HF.get(l, 1.0) for l in args.languages}

    # Write Markdown report
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(render_markdown_report(result, byte_premia), encoding="utf-8")

    # Write YAML config
    args.config_output.parent.mkdir(parents=True, exist_ok=True)
    args.config_output.write_text(render_yaml_config(result, byte_premia), encoding="utf-8")

    # Print headline to stdout
    print()
    print("================================================================")
    print(f"  URIEL typological prior derived for: {', '.join(result.languages)}")
    print("================================================================")
    print(f"  Features used (after unknowns dropped): {result.n_features_used}")
    print(f"  Temperature: {result.temperature}   Floor: {result.floor}")
    print()
    print("  Pairwise cosine distance:")
    for i, li in enumerate(result.languages):
        row = "    " + " ".join(
            f"d({li},{lj})={result.pairwise_distance[i, j]:.4f}"
            for j, lj in enumerate(result.languages) if i != j
        )
        print(row)
    print()
    print("  Initial sampling probabilities pi_0 (over TOKENS):")
    for l, p in zip(result.languages, result.pi_0):
        bar = "█" * int(round(p * 50))
        print(f"    {l}  {p:6.4f}  {bar}")
    print()
    print(f"  Report:  {args.output}")
    print(f"  Config:  {args.config_output}")
    print()
    return 0


if __name__ == "__main__":
    sys.exit(main())
