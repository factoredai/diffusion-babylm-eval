"""Compute the reverse typological prior (P2 negative-control distribution).

Reads configs/typological_prior.yaml, inverts the isolation scores, runs the
same softmax + floor projection, and writes the resulting pi into
configs/P2_reverse_prior.yaml.
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import numpy as np
import yaml

REPO_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO_ROOT))

from taam.exp3 import _apply_floor, _safe_softmax  # noqa: E402


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--prior-config",
        type=Path,
        default=REPO_ROOT / "configs/typological_prior.yaml",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=REPO_ROOT / "configs/P2_reverse_prior.yaml",
    )
    parser.add_argument("--temperature", type=float, default=0.5)
    parser.add_argument("--floor", type=float, default=0.10)
    args = parser.parse_args()

    cfg = yaml.safe_load(args.prior_config.read_text(encoding="utf-8"))
    tp = cfg["typological_prior"]
    isolation_map = tp["isolation"]
    languages = list(isolation_map.keys())
    iso = np.array([isolation_map[l] for l in languages], dtype=np.float64)

    # Reverse: softmax(-isolation / T) with floor projection.
    pi = _safe_softmax(-iso / args.temperature)
    pi = _apply_floor(pi, floor=args.floor)

    # Round to 6 decimals and let the LARGEST entry absorb the residual so the
    # written YAML sums to exactly 1.0 (downstream config validators are strict).
    pi_rounded = np.round(pi, 6)
    residual = 1.0 - pi_rounded.sum()
    pi_rounded[int(pi_rounded.argmax())] += residual
    pi = pi_rounded

    print("Languages:", languages)
    print("Isolation:", iso.tolist())
    print("Reverse pi (token-share):", [float(p) for p in pi])
    print(f"Sum: {pi.sum():.6f}, Min: {pi.min():.6f} (floor={args.floor})")

    # Render YAML
    lines = [
        "# P2 — Reverse typological prior (sanity / negative control)",
        "# Static pi that gives the LEAST mass to the most typologically isolated",
        "# language. This tests whether TAAM's gains genuinely come from the",
        "# prior's direction (not from any reweighting being helpful).",
        "#",
        "# AUTO-GENERATED by scripts/compute_reverse_prior.py. DO NOT EDIT BY HAND.",
        'include: "base.yaml"',
        "",
        "condition:",
        '  id: "P2"',
        '  name: "Reverse typological prior"',
        "  description: |",
        "    Static pi obtained by flipping isolation scores: lang with the highest",
        "    isolation gets the LOWEST share. If TAAM > P1 > P2, the typological prior",
        "    direction is doing work. If P1 ≈ P2 ≈ TAAM, then any non-uniform shape",
        "    helps and the typological story is wrong.",
        "",
        f"    pi = softmax(-isolation / T={args.temperature}); floor={args.floor}.",
        "  method:",
        '    type: "static"',
        "    pi:",
    ]
    for l, p in zip(languages, pi):
        lines.append(f"      {l}: {float(p):.6f}")
    lines.append("    online: null")
    lines.append("")

    args.output.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote: {args.output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
